/// Verifies a Circom/snarkjs BN254 Groth16 proof using Solana's native
/// alt_bn128 precompile syscalls (~100 K CU: mul 3.5K + add 334 + pairing 72.7K + overhead).
///
/// proof layout (256 bytes):
///   [  0.. 64)  pi_a — G1 (x: 32 B, y: 32 B, big-endian)
///   [ 64..192)  pi_b — G2 (x_im: 32, x_re: 32, y_im: 32, y_re: 32, big-endian)
///   [192..256)  pi_c — G1 (x: 32 B, y: 32 B, big-endian)
///
/// public_values: [tier: u8]  where tier ∈ {1, 2}
///
/// Pairing equation checked:
///   e(A, B) · e(−α, β) · e(−vk_x, γ) · e(−C, δ) == 1
/// where vk_x = IC[0] + tier · IC[1]
///
/// Compiled with --features verify-skip the check is omitted for local tests.
/// NEVER deploy with verify-skip.
fn verify_groth16_proof(proof: &[u8], public_values: &[u8]) -> Result<()> {
    require!(proof.len() == 256, SeelError::InvalidProof);
    require!(!public_values.is_empty(), SeelError::InvalidProof);

    let tier = public_values[0] as u64;

    let pi_a = &proof[0..64];
    let pi_b = &proof[64..192];
    let pi_c = &proof[192..256];

    // ── vk_x = IC[0] + tier * IC[1] ──────────────────────────────────────

    let mut tier_scalar = [0u8; 32];
    tier_scalar[24..].copy_from_slice(&tier.to_be_bytes());

    let mut mul_in = [0u8; 96];
    mul_in[..64].copy_from_slice(&VK_IC1);
    mul_in[64..].copy_from_slice(&tier_scalar);

    let tier_ic = alt_bn128_multiplication(&mul_in)
        .map_err(|_| error!(SeelError::InvalidProof))?;

    let mut add_in = [0u8; 128];
    add_in[..64].copy_from_slice(&VK_IC0);
    add_in[64..].copy_from_slice(&tier_ic);
    let vk_x = alt_bn128_addition(&add_in)
        .map_err(|_| error!(SeelError::InvalidProof))?;

    // ── Negate the three G1 points that appear on the RHS ────────────────
    let neg_alpha = negate_g1(&VK_ALPHA_G1);
    let neg_vk_x  = negate_g1(&vk_x);
    let neg_pi_c  = negate_g1(pi_c);

    // ── Build 4-pair input for alt_bn128_pairing (768 bytes) ─────────────
    //   e(A, B) · e(−α, β) · e(−vk_x, γ) · e(−C, δ) == 1
    let mut pairs = [0u8; 768];

    pairs[  0.. 64].copy_from_slice(pi_a);
    pairs[ 64..192].copy_from_slice(pi_b);

    pairs[192..256].copy_from_slice(&neg_alpha);
    pairs[256..384].copy_from_slice(&VK_BETA_G2);

    pairs[384..448].copy_from_slice(&neg_vk_x);
    pairs[448..576].copy_from_slice(&VK_GAMMA_G2);

    pairs[576..640].copy_from_slice(&neg_pi_c);
    pairs[640..768].copy_from_slice(&VK_DELTA_G2);

    let result = alt_bn128_pairing(&pairs)
        .map_err(|_| error!(SeelError::InvalidProof))?;

    let mut expected = [0u8; 32];
    expected[31] = 1;
    require!(result == expected, SeelError::InvalidProof);

    Ok(())
}

#[program]
pub mod seel {
    use super::*;

    /// Verifies a Circom Groth16 income proof on-chain and mints (or renews) an
    /// attestation soulbound token for `user`.
    ///
    /// 1. On-chain BN254 Groth16 proof verification (~400–600 K CU via precompile).
    /// 2. Creates (or renews) the AttestationAccount PDA for `user`.
    /// 3. On first call: initialises a Token-2022 NonTransferable mint PDA and
    ///    mints one soulbound badge token to the user's associated token account.
    pub fn mint_attestation(
        ctx: Context<MintAttestation>,
        proof: Vec<u8>,          // Groth16 proof bytes (256 bytes: pi_a || pi_b || pi_c)
        public_values: Vec<u8>,  // public signals: [tier: u8]
    ) -> Result<()> {
        // ── 0. On-chain ZK verification ────────────────────────────────────────
        verify_groth16_proof(&proof, &public_values)?;

        require!(!public_values.is_empty(), SeelError::InvalidPublicValues);
        let tier = public_values[0];
        require!(tier == 1 || tier == 2, SeelError::InvalidTier);

        let proof_hash: [u8; 32] = {
            use anchor_lang::solana_program::hash::hash;
            hash(&proof).to_bytes()
        };

        // ── 1. Update attestation PDA ──────────────────────────────────────────
        let clock = Clock::get()?;
        let att = &mut ctx.accounts.attestation;
        att.owner = ctx.accounts.user.key();
        att.issuer = ctx.accounts.authority.key();
        att.tier = tier;
        att.issued_at = clock.unix_timestamp;
        att.expires_at = clock.unix_timestamp + ATTESTATION_DURATION;
        att.proof_hash = proof_hash;
        att.bump = ctx.bumps.attestation;

        // ── 2. Token-2022 soulbound mint (first call only) ────────────────────
        let user_key = ctx.accounts.user.key();
        let mint_bump = ctx.bumps.soulbound_mint;
        let pda_seeds: &[&[u8]] = &[b"soulbound_mint", user_key.as_ref(), &[mint_bump]];

        let mint_info          = ctx.accounts.soulbound_mint.to_account_info();
        let token_account_info = ctx.accounts.soulbound_token_account.to_account_info();
        let authority_info     = ctx.accounts.authority.to_account_info();
        let system_program_info = ctx.accounts.system_program.to_account_info();
        let t22_program_info   = ctx.accounts.token_2022_program.to_account_info();

        if mint_info.lamports() == 0 {
            let rent = Rent::get()?;
            let lamports = rent.minimum_balance(SOULBOUND_MINT_LEN);

            invoke_signed(
                &system_instruction::create_account(
                    authority_info.key,
                    mint_info.key,
                    lamports,
                    SOULBOUND_MINT_LEN as u64,
                    ctx.accounts.token_2022_program.key,
                ),
                &[authority_info.clone(), mint_info.clone(), system_program_info.clone()],
                &[pda_seeds],
            )?;

            invoke(
                &spl_token_2022::instruction::initialize_non_transferable_mint(
                    ctx.accounts.token_2022_program.key,
                    mint_info.key,
                )?,
                &[mint_info.clone(), t22_program_info.clone()],
            )?;

            invoke(
                &spl_token_2022::instruction::initialize_mint2(
                    ctx.accounts.token_2022_program.key,
                    mint_info.key,
                    authority_info.key,
                    None,
                    0,
                )?,
                &[mint_info.clone(), t22_program_info.clone()],
            )?;
        }

        if token_account_info.lamports() == 0 {
            anchor_spl::associated_token::create(CpiContext::new(
                ctx.accounts.associated_token_program.to_account_info(),
                anchor_spl::associated_token::Create {
                    payer:            authority_info.clone(),
                    associated_token: token_account_info.clone(),
                    authority:        ctx.accounts.user.to_account_info(),
                    mint:             mint_info.clone(),
                    system_program:   system_program_info.clone(),
                    token_program:    t22_program_info.clone(),
                },
            ))?;

            invoke(
                &spl_token_2022::instruction::mint_to(
                    ctx.accounts.token_2022_program.key,
                    mint_info.key,
                    token_account_info.key,
                    authority_info.key,
                    &[],
                    1,
                )?,
                &[mint_info.clone(), token_account_info.clone(), authority_info.clone()],
            )?;

            msg!("Soulbound token minted to {}", ctx.accounts.user.key());
        }

        emit!(AttestationMinted {
            owner: att.owner,
            tier,
            expires_at: att.expires_at,
        });
        msg!(
            "Attestation minted: owner={} tier={} expires={}",
            att.owner,
            tier,
            att.expires_at
        );
        Ok(())
    }

    /// Closes an expired attestation account.
    ///
    /// Anyone may call this once the account has passed its `expires_at`.
    /// Lamports are returned to the original user.
    pub fn expire_attestation(ctx: Context<ExpireAttestation>) -> Result<()> {
        let clock = Clock::get()?;
        require!(
            ctx.accounts.attestation.expires_at < clock.unix_timestamp,
            SeelError::NotExpired
        );
        msg!("Attestation expired: owner={}", ctx.accounts.attestation.owner);
        Ok(())
    }

    /// Forcibly revokes an attestation before its expiry.
    ///
    /// Only the original issuer (authority) may call this.
    pub fn revoke_attestation(ctx: Context<RevokeAttestation>) -> Result<()> {
        require!(
            ctx.accounts.authority.key() == ctx.accounts.attestation.issuer,
            SeelError::Unauthorized
        );
        msg!("Attestation revoked: owner={}", ctx.accounts.attestation.owner);
        Ok(())
    }
}

