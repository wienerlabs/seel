/// BN254 base-field prime q (big-endian), used for G1 point negation.
const BN254_Q: [u8; 32] = [
    0x30, 0x64, 0x4e, 0x72, 0xe1, 0x31, 0xa0, 0x29,
    0xb8, 0x50, 0x45, 0xb6, 0x81, 0x81, 0x58, 0x5d,
    0x97, 0x81, 0x6a, 0x91, 0x68, 0x71, 0xca, 0x8d,
    0x3c, 0x20, 0x8c, 0x16, 0xd8, 0x7c, 0xfd, 0x47,
];

/// Negates a G1 affine point: (x, y) → (x, q − y), big-endian 32-byte coordinates.
fn negate_g1(point: &[u8]) -> [u8; 64] {
    let mut out = [0u8; 64];
    out[..32].copy_from_slice(&point[..32]); // x is unchanged
    let y = &point[32..64];
    let mut borrow: i16 = 0;
    for i in (0..32).rev() {
        let sub = BN254_Q[i] as i16 - y[i] as i16 - borrow;
        out[32 + i] = sub.rem_euclid(256) as u8;
        borrow = if sub < 0 { 1 } else { 0 };
    }
    out
}

