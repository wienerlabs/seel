#[account]
pub struct AttestationAccount {
    pub owner:      Pubkey,    // 32
    pub issuer:     Pubkey,    // 32
    pub tier:       u8,        // 1
    pub issued_at:  i64,       // 8
    pub expires_at: i64,       // 8
    pub proof_hash: [u8; 32],  // 32  SHA-256 of raw proof bytes
    pub bump:       u8,        // 1
}

impl AttestationAccount {
    pub const LEN: usize = 8 + 32 + 32 + 1 + 8 + 8 + 32 + 1; // = 122
}

